// C++ program to illustrate printing
// of more than one statement in a
// single cout statement
#include <iostream>
using namespace std;

// Driver Code
int main()
{
	string name = "Akshay";
	int age = 18;

	// Print multiple variable on
	// screen using cout
	cout << "Name : " << name << endl
		<< "Age : " << age << endl;

	return 0;
}
